#include <unistd.h>

int	main(int argc, char **argv)
{
	int	i;

	i = 0;
	if (argc == 2)
	{
		while (*argv[1] != '\0')
		{
			if (*argv[1] == 'z')
				write(1, "a", 1);
			else if (*argv[1] == 'Z')
				write(1, "A", 1);
			else
			{
				if (*argv[1] >= 64 && *argv[1] <= 126)
					*argv[1] = *argv[1] + 1;
				write(1, argv[1], 1);
			}
			argv[1]++;
		}
	}
	write(1, "\n", 1);
}
